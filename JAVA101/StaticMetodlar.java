
public class StaticMetodlar {
	
	public static void main(String[] args) {
	
		System.out.println(Math.PI);
		System.out.println(Math.pow(2, 3)); // üs alır
		System.out.println(Math.sqrt(16));
		System.out.println(Math.min(7,1));
		System.out.println(Math.max(7,1));
		
		float f = 1.6f;
		System.out.println(Math.ceil(f)); // yukarı yuvarlar
		System.out.println(Math.floor(f)); // aşağı yuvarlar
		System.out.println(Math.round(f)); // .5 den büyük eşitse yukarı küçükse aşağı yuvarlar
		System.out.println(Math.random()*10); // 0,1 arası rastgele sayı üretir.
		
		
		
		
		
	}
}
