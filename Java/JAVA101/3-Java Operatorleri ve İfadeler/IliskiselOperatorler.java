
public class IliskiselOperatorler {

	public static void main(String[] args) {
		int x = 10;
		int y = 27;
		System.out.println(x<y);
		System.out.println(x>y);
		System.out.println(x<=y);
		System.out.println(x>=y);
		System.out.println(x==y);
		System.out.println(x!=y);
		boolean b = x!=y;
		System.out.println(b);
		// primitive typelarda değerler karşılaştırılırken.
		// reference typelarda bellekte gösterdikleri adresler karşılaştırılır.

	}

}
